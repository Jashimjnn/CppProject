using pbds = tree<T, null_type, less<T>, rb_tree_tag, tree_order_statistics_node_update>;

class Node
{
public:
    int val;
    Node *left;
    Node *right;
    Node(int val)
    {
        this->val = val;
        this->left = NULL;
        this->right = NULL;
    }
};
void Pre_Order(Node *root)
{
    if (root == NULL)
    {
        return;
    }
    cout << root->val << " ";
    Pre_Order(root->left);
    Pre_Order(root->right);
}
int main()
{
    Node *root = new Node(10);
    Node *a = new Node(20);
    Node *b = new Node(30);
    Node *c = new Node(40);
    Node *d = new Node(50);
    root->left = a;
    root->right = b;
    a->left = c;
    a->right = d;
    Pre_Order(root);
    return 0;
}